const root = document.documentElement;
const langButtons = document.querySelectorAll(".lang-btn");
const menuToggle = document.getElementById("menuToggle");
const siteNav = document.getElementById("siteNav");
const i18nTargets = document.querySelectorAll("[data-i18n]");

function applyLanguage(lang) {
  const locale = messages[lang] ? lang : "zh";
  i18nTargets.forEach((node) => {
    const key = node.dataset.i18n;
    const text = messages[locale][key];
    if (text) {
      node.textContent = text;
    }
  });

  root.lang = locale === "zh" ? "zh-CN" : "en";
  localStorage.setItem("eqs_lang", locale);
  langButtons.forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === locale);
  });
}

langButtons.forEach((btn) => {
  btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
});

menuToggle.addEventListener("click", () => {
  const open = siteNav.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(open));
});

siteNav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    siteNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 }
);

document.querySelectorAll(".reveal").forEach((el) => observer.observe(el));

const saved = localStorage.getItem("eqs_lang");
const browserPrefersZh = (navigator.language || "zh").toLowerCase().startsWith("zh");
const preferred = saved || (browserPrefersZh ? "zh" : "en");
applyLanguage(preferred);

document.getElementById("year").textContent = String(new Date().getFullYear());
